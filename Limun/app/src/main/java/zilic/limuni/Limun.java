package zilic.limuni;

import java.util.Date;

public class Limun {

    private int sifra;
    private String zemlja;
    private Date datum;
    private float kiselost;

    public int getSifra() { return sifra; }

    public void setSifra(int sifra) { this.sifra = sifra; }

    public String getZemlja() { return zemlja; }

    public void setZemlja(String zemlja) { this.zemlja = zemlja; }

    public Date getDatum() { return datum; }

    public void setDatum(Date datum) { this.datum = datum; }

    public float getKiselost() { return kiselost; }

    public void setKiselost(float kiselost) { this.kiselost = kiselost; }
}
